package com.bhyf.pojo.qo.recharge;

import com.rising.common.component.BaseQo;
import com.rising.common.util.CharEncodeUtil;

import java.util.Date;

/**
 * Created by admin on 2016/4/13.
 */
public class RechargeOneCardRecordQo extends BaseQo {
    private String tradeNo;

    private Date beginTime;

    private Date endTime;

    private String payMobile;

    private String cardNo;

    private Double rechMoney;

    private Integer status;

    private String cardHolder;

    private String rechUserId;

    public String getTradeNo() {
        return tradeNo;
    }

    public void setTradeNo(String tradeNo) {
        this.tradeNo = tradeNo;
    }

    public Date getBeginTime() {
        return beginTime;
    }

    public void setBeginTime(Date beginTime) {
        this.beginTime = beginTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public String getPayMobile() {
        return payMobile;
    }

    public void setPayMobile(String payMobile) {
        this.payMobile = payMobile;
    }

    public String getCardNo() {
        return cardNo;
    }

    public void setCardNo(String cardNo) {
        this.cardNo = cardNo;
    }

    public Double getRechMoney() {
        return rechMoney;
    }

    public void setRechMoney(Double rechMoney) {
        this.rechMoney = rechMoney;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getCardHolder() {
        return cardHolder;
    }

    public void setCardHolder(String cardHolder) {
        this.cardHolder = CharEncodeUtil.unicodeToUtf8(cardHolder);
    }

    public String getRechUserId() {
        return rechUserId;
    }

    public void setRechUserId(String rechUserId) {
        this.rechUserId = rechUserId;
    }
}
