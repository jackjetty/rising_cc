	private String envirCode;

	private String account;
	
	private Integer status;

	
	public String getEnvirCode() {
		return envirCode;
	}

	public void setEnvirCode(String envirCode) {
		this.envirCode = envirCode;
	}

	public Integer getEnvirId() {
		return envirId;
	}

